print("Calculadora de média escolar")
print("-------------------------")
# Definindo as notas dos alunos
nota1 = 7.5
nota2 = 8.0
nota3 = 6.5

# Calculando a média
media = (nota1/3) + (nota2/3) + (nota3/3)

# Exibindo o resultado
print("A média escolar das notas:",
      f"\nPrimeira nota: {nota1:.2f}",
      f"\nSegunda nota: {nota2:.2f}",
      f"\nTerceira nota: {nota3:.2f}",
      "\n-------------------------"
      f"\nMédia das notas: {media:.2f}",
      "\n-------------------------",
      "\nCálculo da média realizado com sucesso")
